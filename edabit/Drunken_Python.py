# You need to create two functions to substitute str() and int().
# A function called int_to_str() that converts integers into strings
# and a function called str_to_int() that converts strings into integers.

def int_to_str(num):
    return str(num)

print(int_to_str(4))


def str_to_int(num):
    return int(num)

print(str_to_int("4"))
